/**
 * The <code>ComponentType</code> enum is used to assign types to
 * the nodes.
 *
 *
 * @author Jake Papaspiridakos
 *      email: jake.papaspiridakos@stonybrook.edu
 *      Stony Brook ID: 113325146
 **/

public enum ComponentType {
    /**
     * Button Type, can have text
     */
    Button,

    /**
     * Label Type, can have text
     */
    Label,

    /**
     * Text Area, can have text
     */
    TextArea,

    /**
     * HBox Type, can't have text
     */
    HBox,

    /**
     * VBox Type, can't have text
     */
    VBox,

    /**
     * AnchorPane Type, can't have text
     */
    AnchorPane
}
